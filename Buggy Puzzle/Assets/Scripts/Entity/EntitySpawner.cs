                if (currentLevel[y, x] == 1) {
                    posx = -24.4f + (x * tileSize);
                    posy = 13.6f - (y * tileSize);
                    spawnPos = new Vector2(posx, posy);
                    Instantiate(enemyPrefab, spawnPos, transform.rotation);
