    int[][] RandomizeCoords(int[][] ordered) {
        for (int i = 0; i < ordered.Length - 1; i++) 
        {
            int rand = Random.Range(i, ordered.Length);
            int[] temp = ordered[rand];
            ordered[rand] = ordered[i];
            ordered[i] = temp;
        }
        return ordered;
    }

